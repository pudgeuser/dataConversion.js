// Create a variable named price and assign it the string value "200"
let price = "200";

// Convert price into a number using parseInt()
let priceAsInt = parseInt(price);
console.log(priceAsInt); // Output: 200

// Convert price into a number using Number()
let priceAsNumber = Number(price);
console.log(priceAsNumber); // Output: 200

// Convert price into a number using the unary plus operator
let priceAsUnary = +price;
console.log(priceAsUnary); // Output: 200

// Create a variable named distance and assign it the number value 150
let distance = 150;

// Convert distance into a string using toString()
let distanceAsString = distance.toString();
console.log(distanceAsString); // Output: "150"

// Convert distance into a string using String()
let distanceAsString2 = String(distance);
